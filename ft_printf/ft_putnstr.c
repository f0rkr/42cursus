#include "ft_printf.h"

void	ft_putnstr(char *s, int n)
{
	int i;

	if (!s)
		return ;
	i = 0;
	while (*(s + i) != '\0' && i < n)
	{
		ft_putchar_fdret(*(s + i), 1);
		i++;
	}
}
